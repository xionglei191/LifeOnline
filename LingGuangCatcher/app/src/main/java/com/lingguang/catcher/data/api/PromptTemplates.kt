    fun getLinkPrompt(url: String, pageContent: String, title: String? = null, author: String? = null, publishDate: String? = null, articleType: String = "general"): String {
        val metaBlock = buildString {
            if (!title.isNullOrBlank()) appendLine("标题：$title")
            if (!author.isNullOrBlank()) appendLine("作者：$author")
            if (!publishDate.isNullOrBlank()) appendLine("发布日期：$publishDate")
            appendLine("文章类型：$articleType")
        }
        return """
你是一个专业的信息提炼助手。对以下网页内容进行保守整理，提取最有价值的信息，避免过度推断。

来源链接：$url
$metaBlock
内容：
$pageContent

要求：
1. 识别文章的核心论点和关键论据
2. 提取具体的数据、案例和引用
3. 保留作者的独特见解
4. 过滤广告、导航等无关内容
5. 链接、社交帖文、网页中的时间信息默认视为原文内容的一部分，不要自动转成用户自己的任务、截止日期或日程
6. 如果内容较短、噪声较多或语义不完整，优先极简整理，不要补写原文没有明确表达的结论

输出格式（严格遵循）：
## 🔗 信息漏斗

**核心观点**
- [观点 1：用一句话概括]
- [观点 2：用一句话概括]
- [观点 3：用一句话概括]

**关键数据**
- [数据 1：具体数字和来源]
- [数据 2：具体数字和来源]

**精彩引用**
> [原文中的精彩段落或金句；若无合适内容可留空或简短说明]

**我的思考**
[留空供后续填写]

**原文链接**
$url

注意：只输出 Markdown 正文，不要输出 YAML frontmatter。使用简体中文。
        """.trimIndent()
    }

