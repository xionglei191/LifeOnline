    fun getVoicePrompt(noteType: VoiceNoteType, text: String): String = when (noteType) {
        VoiceNoteType.INSPIRATION -> getInspirationPrompt(text)
        VoiceNoteType.TASK -> getTaskPrompt(text)
        VoiceNoteType.SCHEDULE -> getSchedulePrompt(text)
        VoiceNoteType.LEARNING -> getLearningPrompt(text)
        VoiceNoteType.THOUGHT -> getThoughtPrompt(text)
        VoiceNoteType.EXCERPT -> getExcerptPrompt(text)
        VoiceNoteType.BRAINSTORM -> getBrainstormPrompt(text)
    }

